/*체험학습을 2개 이상 신청한 아이의 정보와 부모의 정보 모두 조회*/

SELECT C.*, P.* FROM TBL_CHILD C
INNER JOIN TBL_PARENT P 
ON P.ID = C.PARENT_ID
WHERE C.ID IN 
(
	SELECT CHILD_ID FROM TBL_APPLY
	GROUP BY CHILD_ID
	HAVING COUNT(FIELD_TRIP_ID) >= 2
);

/*참가자(지원자) 수가 가장 적은 체험학습의 제목과 내용 조회*/

SELECT FT.FIELD_TRIP_TITLE, FT.FIELD_TRIP_CONTENT FROM TBL_FIELD_TRIP FT
WHERE FT.ID = 
(
	SELECT FIELD_TRIP_ID FROM TBL_APPLY A
	GROUP BY FIELD_TRIP_ID
	HAVING COUNT(FIELD_TRIP_ID) =
	(
		SELECT MIN(COUNT(FIELD_TRIP_ID)) FROM TBL_APPLY A
		GROUP BY FIELD_TRIP_ID
	)
);

/*평균 참가자(지원자) 수보다 적은 체험학습의 제목과 내용 조회*/

SELECT FT.FIELD_TRIP_TITLE, FT.FIELD_TRIP_CONTENT FROM TBL_FIELD_TRIP FT
WHERE FT.ID IN
(
	SELECT FIELD_TRIP_ID FROM TBL_APPLY A
	GROUP BY FIELD_TRIP_ID
	HAVING COUNT(FIELD_TRIP_ID) <
		(
		SELECT AVG(COUNT(FIELD_TRIP_ID)) FROM TBL_APPLY A
		GROUP BY FIELD_TRIP_ID
		)
);




